class Math {
  double firstNum, secondNum;
  Math(double firstNum, double secondNum) {
    this.firstNum = firstNum;
    this.secondNum = secondNum;
  }
  double addition() {
    return firstNum + secondNum;
  }

  double subtraction() {
    return firstNum - secondNum;
  }

  double multiplication() {
    return firstNum * secondNum;
  }

  double division() {
    return firstNum / secondNum;
  }
}
